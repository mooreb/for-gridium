public class Pair {
    private final String first;
    private final String second;

    public Pair() {
        first = null;
        second = null;
    }

    public Pair(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }


}
